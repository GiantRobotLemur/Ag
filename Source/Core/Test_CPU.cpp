#include <gtest/gtest.h>

#include "Ag/Core/CPU.hpp"

namespace Ag {

namespace {

////////////////////////////////////////////////////////////////////////////////
// Unit Tests
////////////////////////////////////////////////////////////////////////////////
GTEST_TEST(CPU, GetX86_64ArchVersion)
{
    int version = getX86_64ArchVersion();

#if defined(_MSC_VER) && defined(_M_AMD64)
    EXPECT_GE(version, 1);
#else
    EXPECT_EQ(version, 0);
#endif
}

} // Anonymous namespace

} // namespace Ag
